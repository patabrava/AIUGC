def _parse_utc_timestamp(value: Any) -> Optional[datetime]:
    if not value or not isinstance(value, str):
        return None

    candidate = value.strip()
    if not candidate:
        return None

    if candidate.endswith("Z"):
        candidate = candidate[:-1] + "+00:00"

    try:
        parsed = datetime.fromisoformat(candidate)
    except ValueError:
        return None

    if parsed.tzinfo is None:
        parsed = parsed.replace(tzinfo=timezone.utc)

    return parsed.astimezone(timezone.utc)


def _lease_is_active(metadata: Optional[Dict[str, Any]], *, now: Optional[datetime] = None) -> bool:
    if not metadata:
        return False

    expires_at = _parse_utc_timestamp(metadata.get(VIDEO_POLL_LEASE_EXPIRES_KEY))
    if expires_at is None:
        return False

    current = now or _utc_now()
    return expires_at > current


def _quota_reservation_key(metadata: Optional[Dict[str, Any]]) -> Optional[str]:
    if not metadata:
        return None
    value = metadata.get("quota_reservation_key")
    if not value:
        return None
    return str(value)


