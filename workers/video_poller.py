def _utc_now() -> datetime:
    return datetime.now(timezone.utc)


def _utc_now_iso(now: Optional[datetime] = None) -> str:
    current = now or _utc_now()
    return current.replace(microsecond=0).isoformat().replace("+00:00", "Z")


